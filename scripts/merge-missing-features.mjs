/**
 * Merges scripts/blazly-missing-features-export.txt into generated docs data.
 * Run: node scripts/merge-missing-features.mjs
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import {
  parseMainExport,
  parseArticleFromLines,
  writeGeneratedData,
  insertArticle,
} from "./docs-parser-lib.mjs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");
const MAIN_INPUT = path.join(__dirname, "blazly-doc-export.txt");
const MISSING_INPUT = path.join(__dirname, "blazly-missing-features-export.txt");
const OUTPUT = path.join(ROOT, "src/content/docs/generated-data.ts");

/** 1-based line ranges from blazly-missing-features-export.txt (inclusive start, exclusive end). */
const MISSING_SECTIONS = [
  {
    product: "seo",
    title: "Integrations",
    slug: "integrations",
    start: 2,
    end: 96,
    after: "workspaces",
  },
  {
    product: "geo",
    title: "GEO Landing Page Generator",
    slug: "geo-landing-page-generator",
    start: 248,
    end: 365,
    after: "prompt-ranking",
  },
  {
    product: "geo",
    title: "Knowledge Base",
    slug: "knowledge-base",
    start: 372,
    end: 428,
    after: "my-blogs",
  },
  {
    product: "backlinker",
    title: "CSV Import",
    slug: "csv-import",
    start: 431,
    end: 472,
    after: "campaigns",
  },
  {
    product: "social",
    title: "Integrations",
    slug: "integrations",
    start: 475,
    end: 599,
    after: "onboarding",
  },
  {
    product: "social",
    title: "Profile Enhancer",
    slug: "profile-enhancer",
    start: 606,
    end: 653,
    after: "brand-repository",
  },
  {
    product: "social",
    title: "Omnibox",
    slug: "omnibox",
    start: 656,
    end: 700,
    after: "create-post",
  },
  {
    product: "social",
    title: "Your Posts",
    slug: "your-posts",
    start: 703,
    end: 752,
    after: "calendar",
  },
];

function main() {
  const missingRaw = fs.readFileSync(MISSING_INPUT, "utf8").replace(/\r/g, "");
  const missingLines = missingRaw.split("\n");
  const products = parseMainExport(MAIN_INPUT);

  let added = 0;
  let skipped = 0;

  for (const section of MISSING_SECTIONS) {
    const product = products.find((p) => p.slug === section.product);
    if (!product) {
      throw new Error(`Product not found: ${section.product}`);
    }

    const contentLines = missingLines.slice(section.start - 1, section.end - 1);
    const article = parseArticleFromLines(section.title, contentLines, section.slug);
    const inserted = insertArticle(product, article, section.after);

    if (inserted) {
      added++;
      console.log(`Added: ${product.name} → ${article.title} (${article.slug})`);
    } else {
      skipped++;
      console.log(`Skipped (exists): ${product.name} → ${article.title}`);
    }
  }

  writeGeneratedData(
    products,
    OUTPUT,
    "/** Auto-generated by scripts/merge-missing-features.mjs — do not edit manually */"
  );

  console.log(`\nMerged ${added} articles (${skipped} skipped).`);
  console.log(`Wrote ${OUTPUT}`);

  for (const p of products) {
    console.log(`${p.name}: ${p.articles.length} articles`);
  }
}

main();
